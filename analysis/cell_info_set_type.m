function cell_info=cell_info_set_type()

cell_info=get_list();

gc=cell_info_typedef_gc();
bc=cell_info_typedef_bc();
ac=cell_info_typedef_ac();

check_duplicate([gc.cells]);
check_duplicate([ac.cells]);
check_duplicate([bc.cells]);
check_duplicate_class({[gc.cells],[ac.cells],[bc.cells]});

check_class_conflict([gc.cells],cell_info,1);
check_class_conflict([ac.cells],cell_info,2);
check_class_conflict([bc.cells],cell_info,3);

cell_info=assign_type(cell_info,gc);
cell_info=assign_type(cell_info,ac);
cell_info=assign_type(cell_info,bc);

end
%%
function cell_info=assign_type(cell_info,types)

for i=1:numel(types)
    idx=ismember([cell_info.cell_id],types(i).cells);
    [cell_info(idx).type]=deal(types(i).name);
    [cell_info(idx).annotation]=deal(types(i).annotation);
end

end

function check_class_conflict(cells1,cell_info,cell_class)

cells2=[cell_info([cell_info.class]==cell_class).cell_id];
cells_error=setdiff(cells1,cells2);
if ~isempty(cells_error)
    fprintf('%d cell class error\n',cells_error)
end

if cell_class==1
    cells2=[cell_info(([cell_info.class]==cell_class) & ([cell_info.complete]==1)).cell_id];
    cells_error=setdiff(cells2,cells1);
    if ~isempty(cells_error)
        fprintf('%d cell not used\n',cells_error)
    end
    cells_error=setdiff(cells1,cells2);
    if ~isempty(cells_error)
        fprintf('%d cell not complete\n',cells_error)
    end
end

end

function check_duplicate_class(cell_groups)

for i=1:numel(cell_groups)
    for j=i+1:numel(cell_groups)
        o=intersect(cell_groups{i},cell_groups{j});
        if ~isempty(o)
            for k=1:numel(o)
                fprintf('%d duplicate in %d-%d\n',o(k),i,j);
            end
        end
    end
end

end

function check_duplicate(cells)

uniq_cells=unique(cells);
counts=histc(cells,uniq_cells);
d=uniq_cells(counts>1);
if ~isempty(d)
    fprintf('%d duplicate\n',d);
end

end

